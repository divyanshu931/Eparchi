// This is the doctor_ class
public class doctor_ {

    // Instance variables to store information about a doctor
    String Name, Post, experience, hospital, specification, surl;

    // Getter method for the Name instance variable
    public String getName() {
        return Name;
    }

    // Getter method for the specification instance variable
    public String getSpecification() {
        return specification;
    }

    // Getter method for the experience instance variable
    public String getExperience() {
        return experience;
    }

    // Getter method for the Post instance variable
    public String getPost() {
        return Post;
    }

    // Getter method for the hospital instance variable
    public String getHospital() {
        return hospital;
    }

    // Getter method for the surl instance variable
    public String getSurl() {
        return surl;
    }
}
